    public void handlingLogIn(){
        String username = jTextField1.getText();
        String password = jPasswordField1.getText();
        System.out.println(username+" " + password);
        if(username.isBlank() || password.isBlank()){
            JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không được để trống.");
        }else{
            TAIKHOAN tk;
            try {
                tk = variables.getDB().userAuthenciation(variables.getStatement(), username, password);
                System.out.println(tk);
                if(tk != null){
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    variables.openGDQL(tk);
                }else{
                    JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không đúng vui lòng nhập lại");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }
    
    
